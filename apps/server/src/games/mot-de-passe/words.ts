const ACCENT_AGG = /^[A-Z]+$/

export function normalize(word: string): string {
  return word
    .toUpperCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/┼Æ/g, 'OE')
    .replace(/├å/g, 'AE')
    .replace(/[^A-Z]/g, '')
}

