function classifyWord(word: string): string[] {
  const themes: string[] = []
  for (const [theme, fn] of Object.entries(THEME_FILTERS)) {
    if (fn(word)) themes.push(theme)
  }
  return themes
}

export function buildWords(allWords: string[]): WordsModule {
  const byTheme: Record<string, string[]> = {}
  const themeList = ['quotidien', 'animaux', 'nourriture', 'cinema', 'sport', 'geographie', 'objets', 'absurde', 'mix']
  for (const t of themeList) byTheme[t] = []
  for (const w of allWords) {
    if (!ACCENT_AGG.test(w)) continue
    if (w.length < 4 || w.length > 12) continue
    if (STOP_WORDS.has(w)) continue
    const themes = classifyWord(w)
    if (themes.length === 0) {
      byTheme['quotidien'].push(w)
    } else {
      for (const t of themes) byTheme[t].push(w)
    }
  }
  byTheme['mix'] = allWords.filter((w) => ACCENT_AGG.test(w) && w.length >= 4 && w.length <= 12)
  for (const t of themeList) {
    byTheme[t] = [...new Set(byTheme[t])]
  }
  return { all: allWords, byTheme, themeList }
}

export function loadWords(words: string[]): WordsModule {
  if (!wordsModule) wordsModule = buildWords(words)
  return wordsModule
}

export function getWords(): WordsModule {
  if (!wordsModule) throw new Error('Words module not loaded')
  return wordsModule
}

const DIFFICULTY_LEN: Record<string, [number, number]> = {
  facile: [4, 6],
  normal: [5, 9],
  expert: [7, 12],
}

function shuffle<T>(arr: T[]): T[] {
  const out = [...arr]
  for (let i = out.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[out[i], out[j]] = [out[j], out[i]]
  }
  return out
}

export function pickWords(
  theme: string,
  difficulty: string,
  count: number,
  exclude: Set<string> = new Set(),
  seedWords?: string[],
  mod?: WordsModule
): string[] {
  const m = mod ?? getWords()
  const list = theme === 'mix' || !m.byTheme[theme]
    ? (seedWords ?? m.all)
    : m.byTheme[theme]
  const [min, max] = DIFFICULTY_LEN[difficulty] ?? DIFFICULTY_LEN.normal
  const filtered = list.filter((w) => w.length >= min && w.length <= max && !exclude.has(w))
  if (filtered.length === 0) {
    const fallback = m.all.filter((w) => w.length >= min && w.length <= max && !exclude.has(w))
    return shuffle(fallback).slice(0, count)
  }
  return shuffle(filtered).slice(0, count)
}

export function pickOne(
  theme: string,
  difficulty: string,
  exclude: Set<string> = new Set(),
  seedWords?: string[],
  mod?: WordsModule
): string {
  const arr = pickWords(theme, difficulty, 1, exclude, seedWords, mod)
  return arr[0]
}

export function isForbiddenClue(clue: string, secret: string): { forbidden: boolean; reason?: string } {
  const c = normalize(clue)
  const s = normalize(secret)
  if (!c) return { forbidden: false }
  if (c === s) return { forbidden: true, reason: 'identique' }
  if (c.length >= 3 && s.includes(c)) return { forbidden: true, reason: 'contenu_dans_secret' }
  if (c.length >= 3 && c.includes(s)) return { forbidden: true, reason: 'contient_secret' }
  const prefix = commonPrefix(c, s)
  if (prefix.length >= 4 && Math.min(c.length, s.length) - prefix.length <= 2) {
    return { forbidden: true, reason: 'racine_commune' }
  }
  const lev = levenshtein(c, s)
  if (c.length >= 4 && s.length >= 4 && lev <= 1) {
    return { forbidden: true, reason: 'trop_proche' }
  }
  return { forbidden: false }
}

function commonPrefix(a: string, b: string): string {
  let i = 0
  while (i < a.length && i < b.length && a[i] === b[i]) i++
  return a.slice(0, i)
}

function levenshtein(a: string, b: string): number {
  const m = a.length
  const n = b.length
  if (m === 0) return n
  if (n === 0) return m
  const dp: number[][] = Array.from({ length: m + 1 }, () => new Array(n + 1).fill(0))
  for (let i = 0; i <= m; i++) dp[i][0] = i
  for (let j = 0; j <= n; j++) dp[0][j] = j
  for (let i = 1; i <= m; i++) {
    for (let j = 1; j <= n; j++) {
      const cost = a[i - 1] === b[j - 1] ? 0 : 1
      dp[i][j] = Math.min(
        dp[i - 1][j] + 1,
        dp[i][j - 1] + 1,
        dp[i - 1][j - 1] + cost,
      )
    }
  }
  return dp[m][n]
}
